public class literals {
    public static void main(String[] args) {
        // literals 
        int num1 = 0b101; // specifying binary numbers
        int num2 = 0x7E; // specifying hexadecimal numbers
        int num3 = 100_000_000; //adding underscores to as separators
        double num4 = 56; 
        double num5 = 12e10; //exponential values
        char c = 'd';
        c++;
        System.out.println(c);
        System.out.println(num1+num2+num3);
        System.out.println(num4+num5);

    }
}
